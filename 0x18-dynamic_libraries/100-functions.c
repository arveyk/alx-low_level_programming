#include "main.h"

/**
 * add - sums up two numbers
 * @a: first number
 * @b: second number
 *
 * Return: sum of the numbers
 */
int add(int a, int b)
{

	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the numerical difference of the numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - evaluates the product of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: product of the two numbers
 */
int mul(int a,  int b)
{

	return (a * b);
}

/**
 * div - evaluates the quotient from division of a by b
 * @a: first number
 * @b: second number
 *
 * Return: quotient of a/b
 */
int div(int a, int b)
{

	return (a / b);
}

/**
 * mod - evaluates the remainder of division of a by b
 * @a: first number
 * @b: second number
 *
 * Return: remainder
 */
int mod(int a, int b)
{
	int e;
	e = a % b;
}
